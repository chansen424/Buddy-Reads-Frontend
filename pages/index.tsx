import Head from 'next/head'
import Link from 'next/link'
import useAuth from '../hooks/auth'
import {useState, useEffect, FormEvent} from 'react';
import styles from '../styles/Home.module.css'

interface Group {
  id: string;
  name: string;
  owner: string;
  reads: any[];
  members: Set<any>;
}

export default function Home() {
  const {authenticated, logout} = useAuth();
  const [groups, setGroups] = useState<Group[]>([]);

  const [newGroupInput, setNewGroupInput] = useState('');
  const onSubmit = (e: FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    fetch(
      `http://localhost:3001/groups/`,
      {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${localStorage.getItem('accessToken')}`,
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({name: newGroupInput})
      }
    )
    .then(res => res.json())
    .then(group => setGroups([group, ...groups]))
    .then(() => setNewGroupInput(''));
  }

  useEffect(() => {
    if (authenticated) {
      fetch(
        'http://localhost:3001/groups/',
        {
          headers: {
            'Authorization': `Bearer ${localStorage.accessToken}`
          }
        }
      ).then(res => res.json())
      .then(json => setGroups(json));
    }
  }, [authenticated])

  return (
    <div className={styles.container}>
      <Head>
        <title>Home</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div>
        <div>
          <h1 className={styles.centered}>Home</h1>
          { authenticated ? 
            <button className={styles.logout} onClick={e => logout()}>Logout</button> : 
            <Link href="/login"><a className={styles.signin}>Sign In</a></Link> 
          }
        </div>
        {
          authenticated && <>
            <form onSubmit={onSubmit}>
              <input onChange={e => setNewGroupInput(e.target.value)} value={newGroupInput} className={styles.input} placeholder="New Group Name"></input>
              <button className={styles.groupBtn}>Submit</button>
            </form>
            <h2>Groups</h2>
        {groups.map(group => <p key={group.id}><Link href={`/groups/${group.id}`}><a>{group.name}</a></Link></p>)}
          </>
        }
        
      </div>
    </div>
  )
}
