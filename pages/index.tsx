import Head from "next/head";
import Link from "next/link";
import useAuth from "../hooks/auth";
import { useState, useEffect } from "react";
import styles from "../styles/Home.module.css";
import JoinGroup from "../components/JoinGroup";
import AddGroup from "../components/AddGroup";
import classnames from "classnames";

interface Group {
  id: string;
  name: string;
  owner: string;
  reads: any[];
  members: Set<any>;
}

export default function Home() {
  const { authenticated, logout } = useAuth();
  const [groups, setGroups] = useState<Group[]>([]);

  useEffect(() => {
    if (authenticated) {
      fetch("https://buddy-reads-backend.herokuapp.com/groups/", {
        headers: {
          Authorization: `Bearer ${localStorage.accessToken}`,
        },
      })
        .then((res) => res.json())
        .then((json) => setGroups(json));
    }
  }, [authenticated]);

  return (
    <div className={styles.container}>
      <Head>
        <title>BuddyReads | Home</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div
        className={classnames(styles.main, {
          [styles.justifyEnd]: !authenticated,
          [styles.justifyCenter]: !authenticated,
          [styles.flexRow]: authenticated,
        })}
      >
        {!authenticated && (
          <>
            <div className={styles.circle}></div>
            <div>
              <Link href="/login">
                <a className={styles.signin}>Sign In</a>
              </Link>
              <h1 className={styles.heading}>BuddyReads</h1>
              <h2 className={styles.tagline}>Read More. Spoil Less.</h2>
              <p className={styles.copy}>
                Perfect for bookclubs, chatting about movies, and anything else
                you want spoiler-free.
              </p>
            </div>
          </>
        )}

        {authenticated && (
          <>
            <div>
              <button className={styles.logout} onClick={(e) => logout()}>
                Logout
              </button>
              <JoinGroup />
              <AddGroup />
            </div>
            <div>
              <h1 className={styles.groupsHeader}>My Groups</h1>
              {groups.map((group) => (
                <div className={styles.groupListing} key={group.id}>
                  <Link href={`/groups/${group.id}`}>
                    <a>{group.name}</a>
                  </Link>
                </div>
              ))}
            </div>
          </>
        )}
      </div>
    </div>
  );
}
